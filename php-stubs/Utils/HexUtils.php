<?php

namespace Cryptopp;

abstract class HexUtils
{
    /**
     * Convert binary data into hexadecimal representation
     * @param string $str
     * @return string
     */
    public static function bin2hex($str) {}

    /**
     * Decodes a hexadecimally encoded binary string
     * @param string $str
     * @return string
     */
    public static function hex2bin($str) {}
}
